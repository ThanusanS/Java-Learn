class Operators_08 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Using the addition operator
        int sum = a + b;
        System.out.println("Sum: " + sum);

        // Using the subtraction operator
        int difference = a - b;
        System.out.println("Difference: " + difference);

        // Using the multiplication operator
        int product = a * b;
        System.out.println("Product: " + product);

        // Using the division operator
        int quotient = b / a;
        System.out.println("Quotient: " + quotient);

        // Using the modulus operator
        int remainder = b % a;
        System.out.println("Remainder: " + remainder);

        // Using the increment operator
        a++; // a is now 11
        System.out.println("Incremented a: " + a);

        // Using the decrement operator
        b--; // b is now 19
        System.out.println("Decremented b: " + b);

        // Using the compound assignment operator
        a += 5; // a is now 16
        System.out.println("a after compound assignment: " + a);
        b *= 2; // b is now 38
        System.out.println("b after compound assignment: " + b);
       






    }
}

